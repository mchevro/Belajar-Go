package main

import "fmt"

// Operator Matematika
func main() {
	var result = 10 + 10
	fmt.Println(result)

	var a = 10
	var b = 10
	var c = a * b
	fmt.Println(c)

	// Augmented Assigments
	var i = 10
	i += 10
	fmt.Println(i)

	// Unary Operator
	var z = 1
	z++
	fmt.Println(z)
	var j = 1
	j++
	j++
	fmt.Println(j)

}
